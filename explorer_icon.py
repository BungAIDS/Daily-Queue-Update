def png_bytes() -> bytes:
    return base64.b64decode(_ICON_PNG_B64)


def ico_bytes() -> bytes:
    """Return a Windows .ico containing the PNG-compressed 256px icon.

Modern Windows supports PNG payloads inside ICO containers; this keeps the
repository text-only while still publishing a real .ico beside the Explorer.
"""
    png = png_bytes()
    header = struct.pack("<HHH", 0, 1, 1)
    directory = struct.pack("<BBBBHHII", 0, 0, 0, 0, 1, 32, len(png), 22)
    return header + directory + png
